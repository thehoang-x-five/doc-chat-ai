  async register(email: string, password: string, name: string): Promise<User> {
    // Backend expects: { email, password, full_name }
    // Backend returns: { user: UserResponse, message: string }
    const data = await this.request<{ user: any }>(
      '/api/v1/auth/register',
      { method: 'POST', body: JSON.stringify({ email, password, full_name: name }) }
    );
    // Note: Register doesn't return tokens, user needs to login after
    return this.normalizeUser(data.user);
  }

  async login(email: string, password: string): Promise<User> {
    // Backend expects: { email, password }
    // Backend returns: { access_token, refresh_token, token_type, expires_in, user }
    const data = await this.request<{
      access_token: string;
      refresh_token: string;
      token_type: string;
      expires_in: number;
      user: any;
    }>(
      '/api/v1/auth/login',
      { method: 'POST', body: JSON.stringify({ email, password }) }
    );

    this.saveTokens({
      accessToken: data.access_token,
      refreshToken: data.refresh_token,
      tokenType: data.token_type,
      expiresIn: data.expires_in,
    });

    return this.normalizeUser(data.user);
  }

  async logout(): Promise<void> {
    try {
      await this.request('/api/v1/auth/logout', { method: 'POST' }, true);
    } finally {
      this.clearTokens();
    }
  }

  private async refreshAccessToken(): Promise<boolean> {
    try {
      const data = await this.request<{
        access_token: string;
        refresh_token: string;
        token_type: string;
        expires_in: number;
        user: any;
      }>('/api/v1/auth/refresh', {
        method: 'POST',
        body: JSON.stringify({ refresh_token: this.refreshToken }),
      });

      this.saveTokens({
        accessToken: data.access_token,
        refreshToken: data.refresh_token,
        tokenType: data.token_type,
        expiresIn: data.expires_in,
      });
      return true;
    } catch {
      this.clearTokens();
      return false;
    }
  }

