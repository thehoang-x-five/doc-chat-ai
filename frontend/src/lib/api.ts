  /**
   * Send message with SSE streaming for real-time token display.
   * Uses the /api/v1/chat/stream endpoint.
   */
  async sendMessageStream(
    conversationId: string,
    content: string,
    options: {
      model?: string;
      tags?: string[];
      documentIds?: string[];
      workspaceId: string;
      onProgress?: (step: string, progress: number, message?: string) => void;
      onToken?: (token: string, index: number) => void;
      onMetadata?: (metadata: { model?: string; provider?: string; citations?: Citation[]; quality?: Record<string, any>; quality_warnings?: any[] }) => void;
      onError?: (message: string, code?: string) => void;
      onDone?: (totalTimeMs: number) => void;
      onQualityWarning?: (warning: { type: string; severity: string; message: string }) => void;
      onReplaceResponse?: (newContent: string) => void;
    }
  ): Promise<void> {
    const url = `${this.baseUrl}/api/v1/chat/stream`;

    const response = await fetch(url, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${this.accessToken}`,
        'Accept': 'text/event-stream',
      },
      body: JSON.stringify({
        question: content,
        workspace_id: options.workspaceId,
        conversation_id: conversationId,
        model: options.model,
        tags: options.tags,
        document_ids: options.documentIds,
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      throw new Error(`Stream failed: ${errorText}`);
    }

    const reader = response.body?.getReader();
    if (!reader) throw new Error('ReadableStream not supported');

    const decoder = new TextDecoder();
    let buffer = '';
    let currentEventType = '';

    try {
      while (true) {
        const { done, value } = await reader.read();
        if (done) break;

        buffer += decoder.decode(value, { stream: true });
        const lines = buffer.split('\n');
        buffer = lines.pop() || '';

        for (const line of lines) {
          // Track SSE event type
          if (line.startsWith('event:')) {
            currentEventType = line.slice(6).trim();
            continue;
          }

          if (line.startsWith('data:')) {
            const dataStr = line.slice(5).trim();
            if (!dataStr || dataStr === '{}') continue;

            try {
              const data = JSON.parse(dataStr);

              // Route based on event type (preferred) or data fields (fallback)
              switch (currentEventType) {
                case 'progress':
                  options.onProgress?.(data.step, data.progress, data.message);
                  break;
                case 'token':
                  options.onToken?.(data.content, data.index);
                  break;
                case 'metadata':
                  options.onMetadata?.({
                    model: data.model,
                    provider: data.provider,
                    citations: data.citations?.map((c: any) => ({
                      chunkId: c.chunk_id || c.chunkId,
                      documentId: c.document_id || c.documentId,
                      documentTitle: c.document_title || c.documentTitle || '',
                      content: c.content || '',
                      page: c.page,
                      score: c.score,
                    })),
                    quality: data.quality,
                    quality_warnings: data.quality_warnings,
                  });
                  break;
                case 'error':
                  options.onError?.(data.message, data.code);
                  break;
                case 'done':
                  options.onDone?.(data.total_time_ms);
                  break;
                case 'quality_warning':
                  options.onQualityWarning?.(data);
                  break;
                case 'replace_response':
                  options.onReplaceResponse?.(data.content);
                  break;
                default:
                  // Fallback: detect by data fields (backward compat)
                  if (data.step !== undefined && data.progress !== undefined) {
                    options.onProgress?.(data.step, data.progress, data.message);
                  } else if (data.content !== undefined && data.index !== undefined) {
                    options.onToken?.(data.content, data.index);
                  } else if (data.model !== undefined || data.provider !== undefined || data.citations !== undefined) {
                    options.onMetadata?.({
                      model: data.model,
                      provider: data.provider,
                      citations: data.citations?.map((c: any) => ({
                        chunkId: c.chunk_id || c.chunkId,
                        documentId: c.document_id || c.documentId,
                        documentTitle: c.document_title || c.documentTitle || '',
                        content: c.content || '',
                        page: c.page,
                        score: c.score,
                      })),
                      quality: data.quality,
                      quality_warnings: data.quality_warnings,
                    });
                  } else if (data.message !== undefined && data.code !== undefined) {
                    options.onError?.(data.message, data.code);
                  } else if (data.total_time_ms !== undefined) {
                    options.onDone?.(data.total_time_ms);
                  }
                  break;
              }

              // Reset event type after processing
              currentEventType = '';
            } catch (e) {
              console.warn('Failed to parse SSE data:', dataStr, e);
            }
          }
        }
      }
    } finally {
      reader.releaseLock();
    }
  }

