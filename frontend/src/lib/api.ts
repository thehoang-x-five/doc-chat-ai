export async function pollJobStatus(
  jobId: string,
  onProgress?: (progress: { step: string; percent: number; message: string }) => void
): Promise<OcrResult> {
  const maxAttempts = 120;
  let attempts = 0;

  while (attempts < maxAttempts) {
    try {
      const jobStatus = await apiClient.getJob(jobId);
      onProgress?.({ step: jobStatus.step, percent: jobStatus.percent, message: jobStatus.message });
      if (jobStatus.status === 'done' && jobStatus.result) return jobStatus.result;
      if (jobStatus.status === 'error') throw new Error(jobStatus.error || 'Job failed');
      await delay(1000);
      attempts++;
    } catch (error) {
      console.error('Error polling job status:', error);
      attempts++;
      await delay(1000);
    }
  }
  throw new Error('Job timeout - processing took too long');
}

export async function createBatchJobsWithBackend(files: File[], settings: OcrSettings): Promise<BatchJob[]> {
  const newJobs: BatchJob[] = [];

  for (const file of files) {
    try {
      const jobId = await startAsyncOcrJob(file, settings);
      const batchJob: BatchJob = {
        id: jobId,
        fileName: file.name,
        type: 'ocr',
        status: 'queued',
        progress: 0,
        createdAt: new Date().toISOString(),
        updatedAt: new Date().toISOString(),
        attempt: 1,
        message: `Parser: ${settings.parser || 'docling'}`,
      };
      newJobs.push(batchJob);

      pollJobStatus(jobId, (progress) => {
        const job = jobs.find(j => j.id === jobId);
        if (job) {
          job.status = progress.step as BatchJob['status'];
          job.progress = progress.percent;
          job.message = progress.message;
          job.updatedAt = new Date().toISOString();
        }
      }).then(() => {
        const job = jobs.find(j => j.id === jobId);
        if (job) { job.status = 'done'; job.progress = 100; job.updatedAt = new Date().toISOString(); job.resultUrl = `#job-${jobId}`; }
      }).catch((error) => {
        const job = jobs.find(j => j.id === jobId);
        if (job) { job.status = 'done'; job.progress = 100; job.message = `Error: ${error.message}`; job.updatedAt = new Date().toISOString(); }
      });
    } catch (error) {
      const id = generateId('job');
      newJobs.push({ id, fileName: file.name, type: 'ocr', status: 'queued', progress: 0, createdAt: new Date().toISOString(), updatedAt: new Date().toISOString(), attempt: 1, message: `Fallback mode: ${error}` });
      simulateJob(id);
    }
  }

  jobs = [...newJobs, ...jobs].slice(0, 20);
  return newJobs;
}

