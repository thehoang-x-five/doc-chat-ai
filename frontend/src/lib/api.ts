  async uploadDocument(workspaceId: string, file: File, tags?: string[]): Promise<Document> {
    // Try presigned upload (MinIO/S3), fall back to FormData upload (local storage)
    try {
      const presignReq = {
        filename: file.name,
        size: file.size,
        mime_type: file.type || undefined,
        tags: tags || [],
      };

      interface PresignedResponse { document_id: string; upload_url: string; }
      const presigned = await this.request<PresignedResponse>(
        `/api/v1/workspaces/${workspaceId}/documents/presigned-upload`,
        { method: 'POST', body: JSON.stringify(presignReq) },
        true
      );

      // If backend returns a file:// URL (local storage), fall back to FormData upload
      if (presigned.upload_url.startsWith('file://')) {
        // Delete the placeholder document created by presigned flow
        try { await this.request(`/api/v1/documents/${presigned.document_id}`, { method: 'DELETE' }, true); } catch { /* ignore */ }
        return this._uploadViaFormData(workspaceId, file, tags);
      }

      // Presigned flow: PUT directly to MinIO/S3
      const uploadRes = await fetch(presigned.upload_url, {
        method: 'PUT',
        body: file,
        headers: { 'Content-Type': file.type || 'application/octet-stream' },
      });

      if (!uploadRes.ok) {
        throw new Error(`Direct storage upload failed: ${uploadRes.status}`);
      }

      const docData = await this.request<any>(
        `/api/v1/documents/${presigned.document_id}/presigned-confirm`,
        { method: 'POST' },
        true
      );
      return this.normalizeDocument(docData);
    } catch (err) {
      // If presigned flow fails entirely, fall back to FormData
      console.warn('Presigned upload failed, falling back to FormData:', err);
      return this._uploadViaFormData(workspaceId, file, tags);
    }
  }

  private async _uploadViaFormData(workspaceId: string, file: File, tags?: string[]): Promise<Document> {
    const formData = new FormData();
    formData.append('file', file);
    if (tags && tags.length > 0) {
      formData.append('tags', JSON.stringify(tags));
    }

    // Use fetch directly — this.request() forces Content-Type: application/json
    // which breaks multipart/form-data uploads
    const headers: Record<string, string> = {};
    if (this.accessToken) {
      headers['Authorization'] = `Bearer ${this.accessToken}`;
    }

    const response = await fetch(`${this.baseUrl}/api/v1/workspaces/${workspaceId}/documents/upload`, {
      method: 'POST',
      body: formData,
      headers,
    });

    if (!response.ok) {
      const errorText = await response.text().catch(() => 'Upload failed');
      throw new Error(errorText);
    }

    const docData = await response.json();
    return this.normalizeDocument(docData);
  }

